public class casting {
    public static void main(String[] args) {

        double pi = 3.14;
        int i = (int) pi;// casted => moldado - moldar um número do tipo double em um formato do tipo int
        System.out.println("O valor arredondado de pi é?\n" + i);

        long x = 10000;
        int ii = (int) x;
        System.out.println(ii);
    }
}