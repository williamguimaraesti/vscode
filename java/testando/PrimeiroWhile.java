public class PrimeiroWhile {
    public static void main(String[] args) {
        /*While é um comando usado para fazer laço(Loop),
        isto é, repetir um trecho de código algumas vezes.
        Isso ocorrerá enquanto a determina condição permanecer verdadeira.*/

        int idade = 1;
        while(idade <= 18){
            System.out.println(idade);
            idade = idade + 2;
        }       
    }
}
