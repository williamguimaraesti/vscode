public class TiposCalculo {
    public static void main(String[] args) {
        int quatro = 2+2;
        int tres = 5 -2;
        int oito = 4 * 2;
        int dezesseis = 64 / 4;
        int um = 5 % 2;
        System.out.println(quatro);//Soma
        System.out.println(tres);//Subtração
        System.out.println(oito);//Multiplicação
        System.out.println(dezesseis);//Divisão
        System.out.println(um);//Modulo =>Mostra o resto da divisão
    }
    
}
